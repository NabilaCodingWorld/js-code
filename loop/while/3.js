/***

Subtask-1:

Display sum of all the odd numbers from 81 to 131.

 */

let n = 81;
let sum = 0;

while (n <= 131) {
  // console.log(n);
  sum = sum + n; // Add the current odd number to the sum
  n = n + 2;
}

console.log(sum);

